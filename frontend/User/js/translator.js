// ✅ Fill dropdown options automatically
const langSelect = document.getElementById('languageSelect');
langSelect.innerHTML = ""; // Clear if existing options
Object.entries(supportedLanguages).forEach(([code, name]) => {
    const option = document.createElement('option');
    option.value = code;
    option.textContent = name;
    langSelect.appendChild(option);
});

// ✅ Main translate button logic
document.getElementById('translateBtn').addEventListener('click', async () => {
    const text = document.getElementById('textInput').value;
    const to = document.getElementById('languageSelect').value;

    if (!text) {
        alert('Please enter text to translate.');
        return;
    }

    try {
        const response = await fetch('http://localhost:3000/api/translate', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ text, to }) // From is auto-detected by backend
        });

        const data = await response.json();

        if (response.ok) {
            document.getElementById('result').innerHTML = `
                <p><strong>Detected Language:</strong> ${data.detected}</p>
                <p><strong>Translation:</strong> ${data.translated}</p>
            `;
        } else {
            document.getElementById('result').innerText = 'Error: ' + data.error;
        }
    } catch (err) {
        console.error(err);
        document.getElementById('result').innerText = 'Failed to connect to the server.';
    }
});
